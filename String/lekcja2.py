course = "Python Programming"   # Zmienna string
print(len(course))              # Wypisuje długość tekstu, czyli ilość znaków w zmiennej course
print(course[0])                # Wypisuje pierwszy znak zmiennej course, czyli 'P'
print(course[-1])               # Wypisuje ostatni znak zmiennej course, czyli 'g'
print(course[0:3])              # Wypisuje znaki od indeksu 0 do 2, czyli 'Pyt'
print(course[0:])               # Wypisuje wszystkie znaki od indeksu 0 do końca, czyli 'Python Programming'
print(course[:3])               # Wypisuje znaki od początku do indeksu 2, czyli 'Pyt'
print(course[:])                # Wypisuje wszystkie znaki, czyli 'Python Programming'
